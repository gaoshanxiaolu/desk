#ifndef __ATHTYPES_H__
#define __ATHTYPES_H__

#include <inttypes.h>

/* Map from standard C basic integer types to basic integer types used by this SW */

typedef int64_t  A_INT64;
typedef int32_t  A_INT32;
typedef int16_t  A_INT16;
typedef int8_t   A_INT8;
typedef int8_t   A_CHAR;

typedef uint64_t A_UINT64;
typedef uint32_t A_UINT32;
typedef uint16_t A_UINT16;
typedef uint8_t  A_UINT8;
typedef int8_t   A_UCHAR;

typedef int32_t  A_BOOL;

#endif /* __ATHTYPES_H__ */
